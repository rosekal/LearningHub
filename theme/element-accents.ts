export interface ElementAccentPalette {
  id: string;
  label: string;
  accent: string;
  accentStrong: string;
  accentSoft: string;
  accentMuted: string;
  accentContrast: string;
  panel: string;
  line: string;
  glow: string;
  heroFrom: string;
  heroTo: string;
}

type AccentMap = Record<string, ElementAccentPalette>;

interface Rgb {
  r: number;
  g: number;
  b: number;
}

export interface ElementBackdropPalette {
  canvas: string;
  gradientStart: string;
  gradientMid: string;
  gradientEnd: string;
  overlayStart: string;
  overlayEnd: string;
  divider: string;
}

function hexToRgb(hex: string): Rgb {
  const normalized = hex.replace('#', '');
  const value =
    normalized.length === 3
      ? normalized
          .split('')
          .map((part) => `${part}${part}`)
          .join('')
      : normalized;

  return {
    r: Number.parseInt(value.slice(0, 2), 16),
    g: Number.parseInt(value.slice(2, 4), 16),
    b: Number.parseInt(value.slice(4, 6), 16),
  };
}

function rgbToHex({ r, g, b }: Rgb) {
  return `#${[r, g, b]
    .map((value) => Math.max(0, Math.min(255, Math.round(value))).toString(16).padStart(2, '0'))
    .join('')}`;
}

function mixHex(base: string, tint: string, ratio: number) {
  const baseRgb = hexToRgb(base);
  const tintRgb = hexToRgb(tint);
  const clampedRatio = Math.max(0, Math.min(1, ratio));

  return rgbToHex({
    r: baseRgb.r + (tintRgb.r - baseRgb.r) * clampedRatio,
    g: baseRgb.g + (tintRgb.g - baseRgb.g) * clampedRatio,
    b: baseRgb.b + (tintRgb.b - baseRgb.b) * clampedRatio,
  });
}

function withAlpha(hex: string, alpha: number) {
  const { r, g, b } = hexToRgb(hex);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

function tintHex(hex: string, ratio: number) {
  return mixHex(hex, '#ffffff', ratio);
}

function shadeHex(hex: string, ratio: number) {
  return mixHex(hex, '#000000', ratio);
}

function buildTonalBackdrop(theme: AppTheme, baseTone: string, divider: string): ElementBackdropPalette {
  const highlight = theme.mode === 'dark' ? tintHex(baseTone, 0.58) : tintHex(baseTone, 0.68);
  const midTone = theme.mode === 'dark' ? tintHex(baseTone, 0.22) : tintHex(baseTone, 0.24);
  const shadow = theme.mode === 'dark' ? shadeHex(baseTone, 0.5) : shadeHex(baseTone, 0.46);
  const overlayStart = theme.mode === 'dark' ? tintHex(baseTone, 0.42) : tintHex(baseTone, 0.44);
  const overlayEnd = theme.mode === 'dark' ? shadeHex(baseTone, 0.36) : shadeHex(baseTone, 0.34);

  return {
    canvas: baseTone,
    gradientStart: highlight,
    gradientMid: midTone,
    gradientEnd: shadow,
    overlayStart,
    overlayEnd,
    divider,
  };
}

function buildFallback(theme: AppTheme): ElementAccentPalette {
  return {
    id: 'learnhub',
    label: 'LearnHub',
    accent: theme.colors.accent,
    accentStrong: theme.colors.accentStrong,
    accentSoft: theme.colors.accentSoft,
    accentMuted: theme.colors.accentMuted,
    accentContrast: theme.colors.textInverse,
    panel: theme.mode === 'dark' ? '#14314B' : '#E7F0FA',
    line: theme.mode === 'dark' ? '#38597B' : '#8CA7C4',
    glow: theme.colors.accentGlow,
    heroFrom: theme.mode === 'dark' ? '#091724' : '#10233C',
    heroTo: theme.mode === 'dark' ? '#173554' : '#1C4167',
  };
}

function aliasAccent(source: ElementAccentPalette, id: string, label: string): ElementAccentPalette {
  return {
    ...source,
    id,
    label,
  };
}

